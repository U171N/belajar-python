a = False
b = True

c = a & b  # operator logika AND(&)
print("A = ", a)
print("B = ", b)
print("")
print("hasil operasi logika &(and) A & B = ", a & b)  # kedua nilai harus sama
print("")
# ini akan memilih salah satu opsi jawaban
print("hasil operasi logika OR(|) A OR B = ", a | b)
print("")
# jika C tidak sama dengan A(False),maka nilainya True
print("Jika c ~(not) A maka= ", not a)
print("")
# jika C tidak sama dengan B(True),maka nilainya False
print("jika c ~(and) B maka= ", not b)
