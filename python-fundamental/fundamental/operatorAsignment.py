a = 7
b = 2
print("nilai A =", a)
print("nilai B =", b)
print("")

# operator assignment penambahan(+=)
b += a
print("hasil operasi assignment penambahan(+=) =", b)
print("")

# operator assignment perkalian(*=)
b *= a
print("hasil operasi assignment perkalian(*=) =", b)
print("")

# operator assignment pembagian(/=)
b /= a
print("hasil operasi assignment pembagian(/=) =", b)
print("")

# operator assignment pangkat(**=)
b **= a
print("hasil operasi assignment pangkat(**=) =", b)
print("")

# operator assignment pembagian(//=) tapi pembagian ini hasilnya akan dibulatkan
b //= a
print("hasil operasi assignment pembagian ke bilangan bulat(//=) =", b)
print("")
# operator assignment modulus(%=) atau sisa hasil bagi
b %= a
print("hasil operasi assignment modulus(%=) atau sisa hasil bagi =", b)
print("")
